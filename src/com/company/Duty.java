package com.company;

public class Duty {

    private String DutyID;
    private String DutyName;

    public String getDutyID() {
        return DutyID;
    }

    public void setDutyID(String dutyNumber) {
        DutyID = dutyNumber;
    }

    public String getDutyName() {
        return DutyName;
    }

    public void setDutyName(String dutyName) {
        DutyName = dutyName;
    }
}
